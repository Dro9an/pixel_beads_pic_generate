// 根据色数获取对应的颜色集合
export const getColorsByCount = (count) => {
  // 转换为数字类型
  const numCount = parseInt(count);
  
  // 如果色数配置存在且有具体的颜色列表
  if (colorCountConfig[numCount]) {
    const config = colorCountConfig[numCount];
    
    // 如果有ids配置，根据id过滤颜色
    if (config.ids && config.ids.length > 0) {
      return beadColors.filter(color => config.ids.includes(color.id));
    }
  }
  
  // 默认返回所有颜色
  return beadColors;
};

// 根据RGB值获取最接近的拼豆颜色
export const getNearestBeadColor = (rgb, colorCount = 291) => {
  // 获取指定色数的颜色集合
  const colors = getColorsByCount(colorCount);
  
  let minDistance = Infinity;
  let nearestColor = colors[0];
  
  for (const color of colors) {
    const distance = Math.sqrt(
      Math.pow(rgb[0] - color.rgb[0], 2) +
      Math.pow(rgb[1] - color.rgb[1], 2) +
      Math.pow(rgb[2] - color.rgb[2], 2)
    );
    
    if (distance < minDistance) {
      minDistance = distance;
      nearestColor = color;
    }
  }
  
  return nearestColor;
};
